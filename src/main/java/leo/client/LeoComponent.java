package leo.client;

// imports

import java.awt.*;


public class LeoComponent {

    /////////////////////////////////////////////////////////////////
    // Properties
    /////////////////////////////////////////////////////////////////
    private int x;
    private int y;
    private final int width;
    private final int height;
    private LeoComponent parent = null;


    /////////////////////////////////////////////////////////////////
    // Constructor
    /////////////////////////////////////////////////////////////////
    public LeoComponent(int newX, int newY, int newWidth, int newHeight) {
        x = newX;
        y = newY;
        width = newWidth;
        height = newHeight;
    }


    /////////////////////////////////////////////////////////////////
    // Are the coordinates within this component
    /////////////////////////////////////////////////////////////////
    public boolean isWithin(int testX, int testY) {
        return (testX >= getScreenX() && testX <= (getScreenX() + getWidth())) &&
                (testY >= getScreenY() && testY <= (getScreenY() + getHeight()));
    }


    /////////////////////////////////////////////////////////////////
    // Get the screen x
    /////////////////////////////////////////////////////////////////
    public int getScreenX() {
        return getX() + (getParent() == null ? 0 : parent.getScreenX());
    }


    /////////////////////////////////////////////////////////////////
    // Get the screen y
    /////////////////////////////////////////////////////////////////
    public int getScreenY() {
        return getY() + (getParent() == null ? 0 : parent.getScreenY());
    }


    /////////////////////////////////////////////////////////////////
    // Draw the component
    /////////////////////////////////////////////////////////////////
    public void draw(Graphics2D g, Frame mainFrame) {
    }


    /////////////////////////////////////////////////////////////////
    // Activate the component
    /////////////////////////////////////////////////////////////////
    public boolean clickAt(int x, int y) {
        return false;
    }

    public boolean within(int x, int y) {
        return false;
    }


    /////////////////////////////////////////////////////////////////
    // Set the parent
    /////////////////////////////////////////////////////////////////
    public void setParent(LeoComponent newParent) {
        parent = newParent;
    }


    /////////////////////////////////////////////////////////////////
    // Set the location
    /////////////////////////////////////////////////////////////////
    public void setLocation(int newX, int newY) {
        x = newX;
        y = newY;
    }


    /////////////////////////////////////////////////////////////////
    // Basic gets
    /////////////////////////////////////////////////////////////////
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public LeoComponent getParent() {
        return parent;
    }
}
