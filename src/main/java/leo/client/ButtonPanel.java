package leo.client;

// imports

public class ButtonPanel extends LeoContainer {

    /////////////////////////////////////////////////////////////////
    // Properties
    /////////////////////////////////////////////////////////////////
    int buttons = 0;


    /////////////////////////////////////////////////////////////////
    // Constructor
    /////////////////////////////////////////////////////////////////
    public ButtonPanel(SideBoard sideBoard) {
        super(
                SideBoard.MARGIN,
                SideBoard.MARGIN,
                sideBoard.getWidth() - (SideBoard.MARGIN * 2),
                400);
    }


    /////////////////////////////////////////////////////////////////
    // Get button position
    /////////////////////////////////////////////////////////////////
    public int getButtonPosition() {
        return buttons * ((ActionButton.getButtonHeight() + SideBoard.MARGIN));
    }


    /////////////////////////////////////////////////////////////////
    // Get button position
    /////////////////////////////////////////////////////////////////
    public void add(LeoComponent newLeoComponent) {
        super.add(newLeoComponent);
        buttons++;
    }


    /////////////////////////////////////////////////////////////////
    // Clear
    /////////////////////////////////////////////////////////////////
    public void clear() {
        buttons = 0;
        super.clear();
    }


    /////////////////////////////////////////////////////////////////
    // Get y override
    /////////////////////////////////////////////////////////////////
    public int getY() {
        return super.getY() + Client.getGameData().getStatPanel().getHeight() + SideBoard.MARGIN;
    }
}