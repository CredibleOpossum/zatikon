package leo.client;

// imports

import leo.shared.Constants;

import java.awt.*;


public class ChatButton extends LeoComponent {

    /////////////////////////////////////////////////////////////////
    // Properties
    /////////////////////////////////////////////////////////////////
    private final PlayerPanel parent;


    /////////////////////////////////////////////////////////////////
    // Constructor
    /////////////////////////////////////////////////////////////////
    public ChatButton(int x, int y, int width, int height, PlayerPanel newParent) {
        super(x, y, width, height);
        parent = newParent;
    }


    /////////////////////////////////////////////////////////////////
    // tells the playerPanel to focus on chat window
    /////////////////////////////////////////////////////////////////
    public boolean clickAt(int x, int y) {
        Client.getImages().playSound(Constants.SOUND_BUTTON);
        parent.chatFocus();
        return true;
    }


    /////////////////////////////////////////////////////////////////
    // Draw the component
    /////////////////////////////////////////////////////////////////
    public void draw(Graphics2D g, Frame mainFrame) {

        g.drawImage(Client.getImages().getImage(Constants.IMG_CHAT_BUTTON), getScreenX(), getScreenY(), getWidth(), getHeight(), mainFrame);

    }
}
