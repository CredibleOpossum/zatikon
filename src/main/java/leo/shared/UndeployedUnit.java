package leo.shared;

// imports

public class UndeployedUnit {

    /////////////////////////////////////////////////////////////////
    // Properties
    /////////////////////////////////////////////////////////////////
    private final short id;
    private int count = 1;
    private final Castle castle;
    //private boolean isPowerUp = false;


    /////////////////////////////////////////////////////////////////
    // Constructor
    /////////////////////////////////////////////////////////////////
    public UndeployedUnit(Unit unit) {
        id = unit.getID();
        castle = unit.getCastle();
        //isPowerUp = unit.isPowerUp();
    }


    /////////////////////////////////////////////////////////////////
    // Get the unit
    /////////////////////////////////////////////////////////////////
    public Unit getUnit() { //if(!isPowerUp)
        return Unit.getUnit(id, castle);
        //else
        //  return Unit.getPowerUp(id,castle);
    }


    /////////////////////////////////////////////////////////////////
    // Get the ID
    /////////////////////////////////////////////////////////////////
    public short getID() {
        return id;
    }


    /////////////////////////////////////////////////////////////////
    // Get the count
    /////////////////////////////////////////////////////////////////
    public int count() {
        return count;
    }

    public void add() {
        count++;
    }

    public void remove() {
        count--;
    }
}
