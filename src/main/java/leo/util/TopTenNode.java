package leo.util;

import leo.server.Player;


public class TopTenNode {
    /////////////////////////////////////////////////////////////////
    // Constants
    /////////////////////////////////////////////////////////////////


    /////////////////////////////////////////////////////////////////
    // Properties
    /////////////////////////////////////////////////////////////////
    private TopTenNode leftNode = null;
    private TopTenNode rightNode = null;
    private final Player player;


    /////////////////////////////////////////////////////////////////
    // Main module
    /////////////////////////////////////////////////////////////////
    TopTenNode(Player newPlayer) {
        player = newPlayer;
    }


    /////////////////////////////////////////////////////////////////
    // Add a player
    /////////////////////////////////////////////////////////////////
    public void add(TopTenNode newNode) {
        if (newNode.getPlayer().getRating() >= player.getRating()) {
            if (leftNode == null)
                leftNode = newNode;
            else
                leftNode.add(newNode);
        } else {
            if (rightNode == null)
                rightNode = newNode;
            else
                rightNode.add(newNode);
        }
    }


    /////////////////////////////////////////////////////////////////
    // Gets
    /////////////////////////////////////////////////////////////////
    public TopTenNode getLeftNode() {
        return leftNode;
    }

    public TopTenNode getRightNode() {
        return rightNode;
    }

    public Player getPlayer() {
        return player;
    }

}
